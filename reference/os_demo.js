const os = require('os');

//Platform
console.log(os.platform());

//CPU Arch
console.log(os.arch());

//CPu Core Info

console.log(os.cpus());

//Free memory

console.log(os.freemem());

//Total memory
console.log(os.totalmem());

//Home dir
console.log(os.homedir());

//Uptime aka runtime
console.log(os.uptime());